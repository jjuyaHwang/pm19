//*exp.1 이름을 10번 출력하기.
function namePrint10(){
    var userName = "jh";
    for(var i = 1; i <= 10; i++){
        document.write(i+"."+userName+"<br>");
    }
}

//*exp.2 이름을 1000번 출력해보기, 1000번 출력중 홀수번째만 나오게 해보기.
function namePrint500(){
    var userName = "jh";
    for(var i = 1; i <= 1000; i+=2){
        document.write(i+"."+userName+"<br>");
    }
}